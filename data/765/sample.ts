// 导出统计函数
export const get765Stats = () => {
  const songs = sampleSongs765;
  const genreCounts = songs.reduce((acc, song) => {
    acc[song.primaryGenre] = (acc[song.primaryGenre] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  return {
    count: songs.length,
    covers: songs.filter((s) => s.isCover).length,
    genreCounts,
    yearRange: {
      min: Math.min(...songs.map((s) => s.firstYear)),
      max: Math.max(...songs.map((s) => s.firstYear)),
    },
  };
};
